// Generate src/portal-visible-plan-ids.js for LEF.
//
// Unlike Mosaic, LEF's Redpoint HQ portal has NO public calendar SPA to mirror
// (portal.lefclimbing.com/lef/n/calendar 404s). So instead of intercepting a
// browser, we derive the allowlist straight from the GraphQL storefront:
//
//   1. StorefrontPlansQuery  → every plan (id, slug, name) in the shared catalog.
//   2. StorefrontCalendarQuery over the next MONTHS_AHEAD, in WINDOW_DAYS chunks,
//      scoped to LEF's facilityId, for all (non-Mosaic) plan ids → the set of
//      plans that actually have scheduled sessions at LEF.
//   3. Drop EVENT_PLAN_DENYLIST slugs (day passes, group-event passes, 1:1
//      private lessons — bookable inventory, not public events).
//
// Usage:
//   node scripts/discover-plan-ids.mjs                 # print sorted planId[] JSON
//   node scripts/discover-plan-ids.mjs --write=src/portal-visible-plan-ids.js
//       Write the JS module. Idempotent — byte-identical when nothing drifted,
//       so the CI workflow can `git diff --quiet` to detect real changes.
//
// Pure fetch (Node 18+) — no Playwright, no browser, nothing to install.

import fs from 'node:fs';
import {
  GRAPHQL_URL,
  LANGUAGE,
  CALENDAR_INPUT_EXTRA,
  WINDOW_DAYS,
  MONTHS_AHEAD,
  PLANS_PAGE_SIZE,
  EVENT_PLAN_DENYLIST,
} from '../src/calendar-config.js';

const args = Object.fromEntries(
  process.argv.slice(2).map((a) => {
    const [k, v] = a.replace(/^--/, '').split('=');
    return [k, v ?? true];
  })
);

const HEADERS = {
  'Content-Type': 'application/json',
  Accept: 'application/json',
  'User-Agent': 'lef-climbing-events-api/1.0 (+https://lefclimbing.com)',
};

const PLANS_QUERY =
  'query StorefrontPlansQuery($first: Int!) { plans(first: $first) { edges { node { id slug name } } } }';
const CAL_QUERY =
  'query StorefrontCalendarQuery($input: CalendarFilter, $language: Language!) { calendar(input: $input) { planId publicTitle shortSummary(language: $language) } }';

async function gql(query, variables, operationName) {
  const res = await fetch(GRAPHQL_URL, {
    method: 'POST',
    headers: HEADERS,
    body: JSON.stringify({ query, variables, operationName }),
  });
  if (!res.ok) throw new Error(`graphql http ${res.status}`);
  const json = await res.json();
  if (json.errors?.length) {
    throw new Error('graphql errors: ' + json.errors.map((e) => e.message).join('; '));
  }
  return json.data;
}

function isoDate(d) {
  return d.toISOString().slice(0, 10);
}

function buildWindows(now = new Date()) {
  const windows = [];
  const horizon = new Date(now);
  horizon.setMonth(horizon.getMonth() + MONTHS_AHEAD);
  let cursor = new Date(now);
  while (cursor < horizon) {
    const end = new Date(cursor);
    end.setDate(end.getDate() + WINDOW_DAYS - 1);
    if (end > horizon) end.setTime(horizon.getTime());
    windows.push({ startDate: isoDate(cursor), endDate: isoDate(end) });
    cursor = new Date(end);
    cursor.setDate(cursor.getDate() + 1);
  }
  return windows;
}

const chunk = (arr, n) =>
  Array.from({ length: Math.ceil(arr.length / n) }, (_, i) => arr.slice(i * n, i * n + n));

async function main() {
  // 1. Plan catalog.
  const plansData = await gql(PLANS_QUERY, { first: PLANS_PAGE_SIZE }, 'StorefrontPlansQuery');
  const plans = plansData.plans.edges.map((e) => e.node);
  const slugById = new Map(plans.map((p) => [p.id, p.slug]));
  const isMosaic = (p) => /\bmos\b|mosaic/i.test(`${p.slug} ${p.name}`);
  const candidateIds = plans.filter((p) => !isMosaic(p)).map((p) => p.id);

  // 2. Which candidates have sessions at LEF's facility over the horizon?
  //    The `calendar` query 500s on huge planId lists, so chunk at 30.
  const withSessions = new Set();
  for (const window of buildWindows()) {
    for (const ids of chunk(candidateIds, 30)) {
      const input = { ...window, planId: ids, ...CALENDAR_INPUT_EXTRA };
      const data = await gql(CAL_QUERY, { input, language: LANGUAGE }, 'StorefrontCalendarQuery');
      for (const row of data.calendar ?? []) withSessions.add(row.planId);
    }
  }

  // 3. Drop bookable-slot plans (day passes, group passes, private lessons).
  const allowlist = [...withSessions].filter((id) => !EVENT_PLAN_DENYLIST.test(slugById.get(id) || '')).sort();

  const json = JSON.stringify(allowlist, null, 2);
  if (args.write) {
    const moduleSrc = [
      '// Auto-generated by scripts/discover-plan-ids.mjs — DO NOT EDIT BY HAND.',
      '// Source: portal.lefclimbing.com/graphql-public (StorefrontCalendarQuery,',
      '// scoped to LEF facilityId, minus the bookable-slot denylist).',
      '//',
      "// These are the plan IDs LEF's marketing calendar shows. Regenerated by the",
      '// calendar-allowlist GitHub Action; when the set drifts, the workflow opens a',
      '// PR with the diff. To preview locally: node scripts/discover-plan-ids.mjs',
      `export const PORTAL_VISIBLE_PLAN_IDS = ${json};`,
      '',
    ].join('\n');
    fs.writeFileSync(args.write, moduleSrc);
    console.error(`wrote ${allowlist.length} planIds → ${args.write}`);
  } else {
    process.stdout.write(json + '\n');
  }
}

main().catch((err) => {
  console.error('DISCOVERY FAILED:', err.message);
  process.exit(2);
});
